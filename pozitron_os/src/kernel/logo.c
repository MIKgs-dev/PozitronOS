// Простая задержка в микросекундах
static void microdelay(uint32_t microseconds) {
    for (volatile uint32_t i = 0; i < microseconds * 10; i++);
}

// Задержка в миллисекундах
static void delay_ms(uint32_t milliseconds) {
    for (volatile uint32_t i = 0; i < milliseconds * 1000; i++);
}

// Интерполяция цвета (плавное изменение)
static uint32_t interpolate_color(uint32_t color_from, uint32_t color_to, 
                                  uint32_t step, uint32_t total_steps) {
    if (step >= total_steps) return color_to;
    
    uint8_t r1 = (color_from >> 16) & 0xFF;
    uint8_t g1 = (color_from >> 8) & 0xFF;
    uint8_t b1 = color_from & 0xFF;
    
    uint8_t r2 = (color_to >> 16) & 0xFF;
    uint8_t g2 = (color_to >> 8) & 0xFF;
    uint8_t b2 = color_to & 0xFF;
    
    uint8_t r = r1 + ((r2 - r1) * step) / total_steps;
    uint8_t g = g1 + ((g2 - g1) * step) / total_steps;
    uint8_t b = b1 + ((b2 - b1) * step) / total_steps;
    
    return (r << 16) | (g << 8) | b;
}

// ============ ОТРИСОВКА ЛОГОТИПА С ПРОЗРАЧНОСТЬЮ ============
static void draw_logo_alpha(uint32_t x, uint32_t y, uint8_t alpha) {
    struct fb_info* fb = vesa_get_info();
    if (!fb || !fb->found) return;
    
    // Цвет с учетом прозрачности (от черного к LOGO_COLOR)
    uint32_t current_color = interpolate_color(0x000000, LOGO_COLOR, alpha, 255);
    
    // Рассчитываем растянутую ширину
    uint32_t stretched_width = LOGO_WIDTH * STRETCH_X;
    
    // Отрисовка
    for (uint32_t py = 0; py < LOGO_HEIGHT; py++) {
        uint32_t screen_y = y + py;
        if (screen_y >= fb->height) break;
        
        for (uint32_t px = 0; px < LOGO_WIDTH; px++) {
            // Растягиваем текущий пиксель по X
            uint32_t start_x = x + (uint32_t)(px * STRETCH_X);
            uint32_t end_x = x + (uint32_t)((px + 1) * STRETCH_X);
            
            // Получаем бит из массива
            uint32_t byte_index = py * (LOGO_WIDTH / 8) + (px / 8);
            if (byte_index >= 8192) continue;
            
            uint8_t bit_position = 7 - (px % 8);
            uint8_t bit_mask = 1 << bit_position;
            
            // Инвертированная проверка (для белого лого на черном)
            if (!(logo_bitmap[byte_index] & bit_mask)) {
                // Рисуем растянутую полоску с текущим цветом
                for (uint32_t sx = start_x; sx < end_x && sx < fb->width; sx++) {
                    vesa_put_pixel(sx, screen_y, current_color);
                }
            }
        }
    }
}

